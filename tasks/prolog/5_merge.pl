$ определить предикат mrg(L1, L2, R) который для двух отсортированных списков L1 и L2 
% определяет список R, составленный из этих элементов

mrg([], [], []).
mrg([H], [], [H]).
mrg([], [H], [H]).
mrg([H|Q], [H1|Q1], R) :-
	H < H1 ->  R = [H|R1], mrg(Q, [H1|Q1], R1);
	H > H1 ->  R = [H1|R1], mrg([H|Q], Q1, R1);
	R = [H,H1|R1], mrg(Q,Q1,R1).

?- mrg([1,2,3,4],[1,4],R).
R = [1, 1, 2, 3, 4, 4]
